# Obtener la fecha y hora actual
# Importamos el modulo de datetime
from datetime import datetime

# Esto te imprime el año, el mes, el dia, la hora, minutos ,segundos y microsegundos.
fecha_actual_del_sistema = datetime.now()
print(fecha_actual_del_sistema)

# Año
anio = fecha_actual_del_sistema.year
print(anio)

# Mes
mes = fecha_actual_del_sistema.month
print(mes)

# Dia
dia = fecha_actual_del_sistema.day
print(dia)

